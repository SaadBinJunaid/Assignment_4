# 💥🎲 Ultimate Random Number Generator Showdown! 🎲💥
# This exciting program will generate 10 awesome random numbers between 1 and 100! 🚀

import random  # 📦 Importing the random module for number magic 🪄

# 🌟 Energetic Title
print("🎉 Welcome to the Random Number Generator Blast! 🎉")
print("✨ Generating 10 thrilling numbers between 1 and 100... Buckle up! 🚀\n")

# 🔁 Loop to generate and display the numbers
for i in range(10):
    number = random.randint(1, 100)  # 🎯 Grab a random number in range
    print(f"👉 Number {i + 1}: {number} 🎯")  # 🖨️ Show the result with energy
