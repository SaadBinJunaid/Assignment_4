# Constants (fixed values that won't change)
DAYS_IN_A_YEAR = 365
HOURS_IN_A_DAY = 24
MINUTES_IN_AN_HOUR = 60
SECONDS_PER_MINUTE = 60

# Calculate the number of seconds in a year
SECONDS_IN_YEAR = DAYS_IN_A_YEAR * HOURS_IN_A_DAY * MINUTES_IN_AN_HOUR * SECONDS_PER_MINUTE

# Display the result
print(f"\nThere are {SECONDS_IN_YEAR} seconds in a year!")
